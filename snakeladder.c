#include <stdio.h>
#include <stdlib.h>
#include <time.h>

#define BOARD_SIZE 100

int rollDie() {
    return rand() % 6 + 1;
}

void printBoard(int player1, int player2) {
    for (int i = 1; i <= BOARD_SIZE; i++) {
        if (i == player1) {
            printf("P1 ");
        } else if (i == player2) {
            printf("P2 ");
        } else {
            printf("%d ", i);
        }
        if (i % 10 == 0) {
            printf("\n");
        }
    }
}

int movePlayer(int position, int roll) {
    int newPosition = position + roll;
    if (newPosition > BOARD_SIZE) {
        return position;
    }
    // Snakes
    if (newPosition == 16) return 6;
    if (newPosition == 47) return 26;
    if (newPosition == 49) return 11;
    if (newPosition == 56) return 53;
    if (newPosition == 62) return 19;
    if (newPosition == 64) return 60;
    if (newPosition == 87) return 24;
    if (newPosition == 93) return 73;
    if (newPosition == 95) return 75;
    if (newPosition == 98) return 78;
    // Ladders
    if (newPosition == 1) return 38;
    if (newPosition == 4) return 14;
    if (newPosition == 9) return 31;
    if (newPosition == 21) return 42;
    if (newPosition == 28) return 84;
    if (newPosition == 36) return 44;
    if (newPosition == 51) return 67;
    if (newPosition == 71) return 91;
    if (newPosition == 80) return 100;
    return newPosition;
}

int main() {
    srand(time(0));
    int player1 = 0, player2 = 0;
    while (player1 < BOARD_SIZE && player2 < BOARD_SIZE) {
        printf("Player 1's turn\n");
        int roll = rollDie();
        printf("Rolled: %d\n", roll);
        player1 = movePlayer(player1, roll);
        printBoard(player1, player2);
        if (player1 >= BOARD_SIZE) {
            printf("Player 1 wins!\n");
            break;
        }

        printf("Player 2's turn\n");
        roll = rollDie();
        printf("Rolled: %d\n", roll);
        player2 = movePlayer(player2, roll);
        printBoard(player1, player2);
        if (player2 >= BOARD_SIZE) {
            printf("Player 2 wins!\n");
            break;
        }
    }
    return 0;
}



